package testanadotipodedadosprimitivos;

/**
 *
 * @author Fernando Gomes
 */
public class Incremento_e_Decremento {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int num = 0;
        
        System.out.println(num);
        
        System.out.println(++num);
        
        System.out.println(num++);
        
        System.out.println(num);
        
    }
    
}
